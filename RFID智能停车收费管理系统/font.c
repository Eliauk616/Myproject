    /**
     * show_num:显示数字
     * x,y :需要显示的位置
     * number:需要显示的数字
     * color ：字体颜色
     * plcd ：屏幕映射mmap（）的返回值
     */
    void show_num(int x,int y,int number,int color,unsigned int *plcd)
    {
        if(number == 0)
        {
            show_font(x,y,24,35,numbers[0],color,plcd);
            return ;
        }
        int s = 0;//记录是几位数
        int num = number;
        //求出这个数是几位数
        while (num)
        {
            s++;
            num = num / 10;
        }
    
        int arr[s];
        for(int i = 0;i < s;i++)
        {
            arr[s-1-i] = number % 10;
            number = number / 10;
        }
    
        //显示数
        for(int i = 0;i < s ; i++)
        {
            show_font(x+(i*24),y, 24, 35, numbers[arr[i]], color, plcd);  
        }
    }



    